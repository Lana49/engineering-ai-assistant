def format_calculation_result(
    formula: str,
    data: dict[str, Any],
    result: float,
    unit: str,
    source: str,
    title: str = "Результат расчёта"
) -> str:
    """Форматирует результат расчёта для вывода."""
    lines = [
        f"📐 **{title}**",
        "",
        f"**Формула:** `{formula}`",
        "",
        "**Исходные данные:**"
    ]

    for key, value in data.items():
        if isinstance(value, float):
            lines.append(f"- {key}: **{value:.2f}**")
        else:
            lines.append(f"- {key}: **{value}**")

    lines.extend([
        "",
        "**Подстановка:**",
        f"`{formula}`",
        "",
        f"**Результат:** **{result:.2f} {unit}**",
        "",
        f"📚 **Источник:** {source}"
    ])

    return "\n".join(lines)


def format_definition_result(
    term: str,
    definition: str,
    source: str,
    example: str | None = None
) -> str:
    """Форматирует определение термина для вывода."""
    lines = [
        f"📖 **Определение термина «{term}»:**",
        "",
        definition,
        "",
        f"📚 **Источник:** {source}"
    ]

    if example:
        lines.extend([
            "",
            "**Пример:**",
            example
        ])

    return "\n".join(lines)


def format_search_result(
    answer: str,
    sources: list[str | dict[str, Any]],
    key_points: list[str] | None = None
) -> str:
    """Форматирует результат поиска для вывода."""
    lines = [
        "📌 **Найдена информация:**",
        "",
        answer
    ]

    if key_points:
        lines.append("")
        lines.append("**Ключевые пункты:**")
        for point in key_points:
            lines.append(f"- {point}")

    if sources:
        lines.append("")
        lines.append("📚 **Источники:**")
        for src in sources[:3]:
            if isinstance(src, dict):
                lines.append(f"- {src.get('doc_name', 'Документ')}")
            else:
                lines.append(f"- {src}")

    return "\n".join(lines)


def format_table_result(table_data: str, title: str, source: str) -> str:
    """Форматирует таблицу для вывода."""
    truncated = table_data[:1000]
    suffix = "" if len(table_data) <= 1000 else "..."

    lines = [
        f"📊 **{title}**",
        "",
        "```",
        f"{truncated}{suffix}",
        "```",
        "",
        f"📚 **Источник:** {source}"
    ]

    return "\n".join(lines)


