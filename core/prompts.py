def get_quick_definition(term: str) -> dict[str, str] | None:
    """Возвращает быстрое определение термина или None."""
    if not term:
        return None

    term_lower = term.lower().strip()

    if term_lower in QUICK_DEFINITIONS:
        return QUICK_DEFINITIONS[term_lower]

    for key, value in QUICK_DEFINITIONS.items():
        if key in term_lower or term_lower in key:
            return value

    return None


def init_prompts() -> dict[str, Any]:
    """Инициализация промптов для обратной совместимости."""
    return {
        "system": get_system_prompt(),
        "definitions": QUICK_DEFINITIONS
    }
